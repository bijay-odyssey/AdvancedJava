public class MathDemo {
    public static void main(String[] args){
        System.out.println("Max: "+ Math.max(10,20));
        System.out.println("Max: "+ Math.min(10,20));
        System.out.println("Sqrt: "+ Math.sqrt(100));
        System.out.println("Power: "+ Math.pow(10,2));
        System.out.println("Abs: "+ Math.abs(-20));
        System.out.println("Pi: "+ Math.PI);
        System.out.println("Random(0-1): "+ Math.random());



    }
}
